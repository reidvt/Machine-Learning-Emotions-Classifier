# Cell 17: Define High / Medium / Low Trust groups and map

# Trust mapping based on project prompt table:
high_trust_labels = [
    'admiration', 'amusement', 'approval', 'caring', 'gratitude', 'joy', 'love', 'optimism'
]

medium_trust_labels = [
    'curiosity', 'desire', 'excitement', 'realization', 'relief', 'surprise', 'pride', 'neutral'
]

low_trust_labels = [
    'anger', 'annoyance', 'disappointment', 'disapproval', 'disgust',
    'embarrassment', 'fear', 'grief', 'nervousness', 'remorse', 'sadness', 'confusion'
]

all_trust_labels = set(high_trust_labels + medium_trust_labels + low_trust_labels)
missing_trust = all_trust_labels - set(label_cols)
print("Missing trust labels (should be empty set):", missing_trust)

def map_to_trust(row):
    hi = row[high_trust_labels].sum()
    med = row[medium_trust_labels].sum()
    lo = row[low_trust_labels].sum()

    total = hi + med + lo
    if total == 0:
        return np.nan  # no emotions at all

    # tie-breaking order: high > medium > low
    if hi >= med and hi >= lo:
        return "high_trust"
    elif med >= hi and med >= lo:
        return "medium_trust"
    else:
        return "low_trust"

