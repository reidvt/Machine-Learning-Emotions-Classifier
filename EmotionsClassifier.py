def show_top_features_for_class(coef_row, class_name, top_n=15):
    # top positive coefficients
    top_pos_idx = np.argsort(coef_row)[-top_n:][::-1]
    # top negative coefficients
    top_neg_idx = np.argsort(coef_row)[:top_n]

    print(f"\n=== Top + words for class '{class_name}' ===")
    for idx in top_pos_idx:
        print(f"{feature_names_sent[idx]:<25} {coef_row[idx]:.3f}")

    print(f"\n=== Top - words for class '{class_name}' (push away) ===")
    for idx in top_neg_idx:
        print(f"{feature_names_sent[idx]:<25} {coef_row[idx]:.3f}")

for i, cls in enumerate(classes_sent):
    show_top_features_for_class(coef_sent[i], cls, top_n=15)

