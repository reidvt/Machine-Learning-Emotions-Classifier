# Cell 10: Map multi-label emotions to 3-class sentiment

def map_to_sentiment(row):
    pos_count = row[positive_labels].sum()
    neg_count = row[negative_labels].sum()
    neu_count = row[neutral_mixed_labels].sum()

    if (pos_count > 0) and (neg_count == 0) and (neu_count == 0):
        return "positive"
    elif (neg_count > 0) and (pos_count == 0) and (neu_count == 0):
        return "negative"
    else:
        # neutral-only, or any combination/mix of types -> Neutral / Mixed bucket
        if (pos_count + neg_count + neu_count) == 0:
            return np.nan  # no emotion labels at all
        return "neutral_mixed"

df["sentiment_3"] = df.apply(map_to_sentiment, axis=1)

